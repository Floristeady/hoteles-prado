			<h1 class="title">Acerca de Coatepec</h1>
			
			<h2 class="subtitle">Coatepec es un apacible  y pintoresco pueblo con una de las más importantes producciones de café del país, y rodeado por el exuberante bosque de niebla.  </h2>
		
			
			<h4>Actividades en Coatepec:</h4>
				<ul>
					<li>Museo del Café. Proceso e historia del café. </li>
					<li>Heladería del Quiosco. Excelentes nieves en el centro de Coatepec. </li> 
					<li>Cascada de Texolo. Impresionante escenario natural. </li>
					<li>Iglesias de San Jerónimo y Santuario de Guadalupe. </li>
					<li>Mercado orgánico Coatl. Mercado de productos orgánicos de la zona. </li>
					<li>	Visita a cercano pueblo de Xico: cañonismo, senderismo y paseos en moto. Productos regionales: mole y licor de morita. </li>
					<li>Visita al cercano pueblo de Jalcomulco:  Descenso de ríos, tirolesa,  rapel y bici de montaña.</li>

				</ul>

			
			<h4>Actividades en Xalapa:</h4>
				<ul>
					<li>Museo de Antropología.- Segundo museo arqueológico más importante de México. </li>
					<li>Hacienda del Lencero.- Preciosa hacienda en las afueras de la ciudad que pertenecía al ex-presidente Santa Ana. </li> 
					<li>Ágora de la Ciudad.- Lugar sede de exposiciones y eventos culturales. </li>
					<li>Parque Juárez.-  Agradable parque con impresionantes vistas del Cofre de Perote. </li>
					<li>Paseo de los lagos.- Relajante área ideal para caminar parte de la zona universitaria.</li>
					<li>Mercado San José.-  Mercado con restaurantes y bares con mesas al aire libre recomendable para la noche. También se recomienda visitar el Callejon cercano de Jesus te ampare.</li>
					<li>Callejón del Diamante.-Pintoresco callejón con variados puestos, tiendas y restaurantes. </li>
				</ul>
				
			
		    	 
