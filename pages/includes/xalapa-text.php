<h2 class="subtitle">Xalapa, capital del estado de Veracruz, es una ciudad universitaria enmarcada por las montañas Cofre de Perote y el Pico de Orizaba. </h2>
		
			<p>Posee atractivos parques, una intensa vida cultural, el Museo de Antropología uno de los museos más importantes del país, y unos alrededores  espectaculares. Se disfruta la vida en sus agradables cafés, sabrosos restaurantes, impresionante naturaleza y magníficos festivales culturales como el del libro,  junio musical, el internacional de jazz en agosto y el Hay festival, entre otros a lo largo del año.</p>
			
			<h4>Actividades en Xalapa:</h4>
				<ul>
					<li>Museo de Antropología.- Segundo museo arqueológico más importante de México. </li>
					<li>Hacienda del Lencero.- Preciosa hacienda en las afueras de la ciudad que pertenecía al ex-presidente Santa Ana. </li> 
					<li>Ágora de la Ciudad.- Lugar sede de exposiciones y eventos culturales. </li>
					<li>Parque Juárez.-  Agradable parque con impresionantes vistas del Cofre de Perote. </li>
					<li>Paseo de los lagos.- Relajante área ideal para caminar parte de la zona universitaria.</li>
					<li>Mercado San José.-  Mercado con restaurantes y bares con mesas al aire libre recomendable para la noche. También se recomienda visitar el Callejon cercano de Jesus te ampare.</li>
					<li>Callejón del Diamante.-Pintoresco callejón con variados puestos, tiendas y restaurantes. </li>
					<li>Museo Interactivo de Xalapa.- Descubra los más interesantes experimentos científicos y asómbrese con su pantalla IMAX, y el observatorio astronómico. </li>
					<li>Orquesta Sinfónica de Xalapa.- Una de las principales orquestas del país.</li> 
					<li>Pinacoteca Diego Rivera,. Exhibición permanente de Diego Rivera y otros pintores. </li> 
					<li>Galería Alba de la Canal.- Exposiciones de artistas locales. </li> 
					<li>Museo Casa de Xalapa .- Museo de historia local con exhibiciones interactivas. </li>
					<li>Galería de Arte contemporáneo.- Exposiciones de artistas locales. </li>
					<li>Museo de Guizar y Valencia.- Conozca la historia de este santo mexicano.</li>
					<li>Jardín Botánico.-  Interesante colección de biodiversidad local e internacional.</li>
					<li>Parque de los Berros.- Un agradable paseo entre árboles centenarios. </li>
				</ul>
				
			<h4>Actividades en los alrededores:</h4>
				<ul>
					<li>Coatepec. Museo del Café.- Proceso e historia del café. </li>
					<li>Cascada de Texolo.- Impresionante escenario natural. </li> 
					<li>Jalcomulco.-  Descenso de ríos, tirolesa,  rapel y bici de montaña. </li>
					<li>Xico.- Cañonismo, senderismo y paseos en moto. Productos regionales: mole y licor de morita. </li>
					<li>Naolinco.- Población dedicada a la manufactura de zapatos y con una excelente comida regional. </li>
				</ul>
