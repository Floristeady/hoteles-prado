<h2 class="subtitle">Xalapa, capital of the state of Veracruz, is a University City surrounded by the Cofre de Perote and Pico de Orizaba mountains.</h2>
		
			<p>It possesses beautiful green parks, a vibrant cultural scene, one of the top Anthropology Museums in Mexico and spectacular surroundings. Enjoy life in one of its charming coffee houses, tasty restaurants, outstanding nature and inspiring cultural festivals like the book festival, the traditional and international music festival in June, the international jazz festival in August and the Hay festival, among others along the year.</p>
			
			<h4>Activities in Xalapa:</h4>
				<ul>
					<li>Museo de Antropología.- One of the best archeological museums in Latinoamerica. </li>
					<li>Hacienda del Lencero.- Wonderfully preserved hacienda that belonged to former president Santa Ana. </li> 
					<li>Agora de la Ciudad.- Meeting point for culture lovers and local artists. </li>
					<li>Parque Juarez.- Charming park with wonderful views of Cofre de Perote.</li>
					<li>Paseo de los Lagos.- Great area for a relaxing walk by the lake.</li>
					<li>Mercado San José.- Market with a section full of open air restaurants and bars, can include a visit to the Callejon de Jesus te ampare alley.</li>
					<li>Callejon del Diamante.- Quaint alley with stalls, restaurants and stores. </li>
					<li>Museo Interactivo de Xalapa (Papalote).- Discover the most impressive scientific experiments and be amazed by the IMAX theater, you can also visit the planetarium. </li>
					<li>Orquesta Sinfónica de Xalapa.- A world class symphonic orchestra. </li> 
					<li>Pinacoteca Diego Rivera.-  Gallery for paintings and sculptures.  </li> 
					<li>Galeria Alba de la Canal.- Local artists exhibits.</li> 
					<li>Museo Casa de Xalapa.- Local history museum with interactive displays.</li>
					<li>Galeria de Arte Contemporaneo.-  Local artists exhibits. </li>
					<li>Museo de Guizar y Valencia.-  Get to know the life of this Mexican saint. </li>
					<li>Jardin Botanico.- Interesting collection and gardens for plant lovers.</li>
					<li>Parque de los Berros.- A nice walk among century-old trees. </li>
				</ul>
				
			<h4>Surroundings:</h4>
				<ul>
					<li>Coatepec. Museo del Café. Coffee history and making process.  </li>
					<li>Xico: Cannyonism, trekking and motorbike tours, delicious cuisine and local products. </li> 
					<li> Cascada de Texolo, a waterfall in an impressive natural scenery </li>
					<li>Naolinco: Town specialized in shoe making and with excellent regional food.</li>
					<li>Jalcomulco:  River rafting, ziplining, rappel and mountain bike. </li>
				</ul>
